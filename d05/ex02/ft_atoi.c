int	ft_atoi(char *str)
{
	int	res;
	int	merkki;

	res = 0;
	merkki = 1;
	while ((*str >= 9 && *str <= 13) || *str == 32)
		str++;
	if (str[0] == '-')
	{
		merkki = -1;
		str++;
	}
	if (*str == '-' || *str == '+')
		str++;
	while (*str != '\0' && (*str >= '0') && *str <= '9')
	{
		res = res * 10 + *str - '0';
		str++;
	}
	return (merkki * res);
}
